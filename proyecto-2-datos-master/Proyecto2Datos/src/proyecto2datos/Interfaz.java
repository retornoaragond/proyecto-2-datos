    private void boton_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_buscarActionPerformed
       
              String[] options=new String[]{"Autor","Tipo", "Nombre","Codigo"};
        int response=JOptionPane.showOptionDialog(null,"Opciones","MODO BUSQUEDA",
                JOptionPane.DEFAULT_OPTION, JOptionPane.PLAIN_MESSAGE,
                null, options,options[0]);
        try{
        switch(response){
            case 0: String cod=JOptionPane.showInputDialog(null, "Ingrese el autor", null); 
                   tree.searchA(cod);
                   break;
                     
            case 1: int ty=Integer.parseInt(JOptionPane.showInputDialog(null, "Ingrese el tipo", null));
                    tree.searchTy(ty);
                    break;
            
            
            case 2: String cad=JOptionPane.showInputDialog(null, "Ingrese el nombre del libro", null);
            tree.searchNa(cad);
            break;
           
            
            case 3: String cud=JOptionPane.showInputDialog(null, "Ingrese el codigo", null);
            tree.searchCo(cud);
            break;
           
               
        }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error al Buscar");
        }
    }//GEN-LAST:event_boton_buscarActionPerformed

