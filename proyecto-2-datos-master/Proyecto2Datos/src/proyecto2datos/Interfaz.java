    private void boton_eliminaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_eliminaActionPerformed
        String[] options=new String[]{"Codigo","Tipo"};
        int response=JOptionPane.showOptionDialog(null,"Opciones","MODO ELIMINA",
                JOptionPane.DEFAULT_OPTION, JOptionPane.PLAIN_MESSAGE,
                null, options,options[0]);
        try{
        if(response==0){
            String cod=JOptionPane.showInputDialog(null, "Ingrese el codigo", null);
            tree.delete_code(cod);
        }
        else{
             int ty=Integer.parseInt(JOptionPane.showInputDialog(null, "Ingrese el Tipo", null));
             tree.delete_type(ty);
        }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error al eliminar");
        }
    }//GEN-LAST:event_boton_eliminaActionPerformed

